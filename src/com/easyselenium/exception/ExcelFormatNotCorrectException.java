package com.easyselenium.exception;

public class ExcelFormatNotCorrectException extends Exception {
	
	private static final long serialVersionUID = 3044379637484280686L;

	public ExcelFormatNotCorrectException() {
		super();
	}

	public ExcelFormatNotCorrectException(String message, Throwable cause) {
		super(message, cause);
	}

	public ExcelFormatNotCorrectException(String message) {
		super(message);
	}

	public ExcelFormatNotCorrectException(Throwable cause) {
		super(cause);
	}
	
}
